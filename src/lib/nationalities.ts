/** Trouver une nationalité par son code */
export function getNationality(code: string): Nationality | undefined {
  return nationalities.find(n => n.code === code)
}

/** Rechercher des nationalités par nom (pour la liste déroulante avec recherche) */
export function searchNationalities(query: string): Nationality[] {
  if (!query || query.length < 1) return nationalities
  const q = query.toLowerCase()
  return nationalities.filter(n =>
    n.name.toLowerCase().includes(q) ||
    n.code.toLowerCase().includes(q)
  )
}
